# 1. String Variable – text
name = "Lakshmi"
city = "Nuzvid"
print("Name:", name)
print("City:", city)

# 2. Integer Variable – whole numbers
age = 18
print("Age:", age)

# 3. Float Variable – decimal numbers
height = 5.6
weight = 55.5
print("Height:", height)
print("Weight:", weight)

# 4. Boolean Variable – True / False
is_student = True
has_passed = False
print("Is Student:", is_student)
print("Has Passed:", has_passed)

# 5. List Variable – multiple values
fruits = ["apple", "banana", "mango"]
marks = [85, 90, 95]
print("Fruits:", fruits)
print("Marks:", marks)

# 6. Dictionary Variable – key-value pairs
person = {"name": "Lakshmi", "age": 20, "city": "Nuzvid"}
print("Person Dictionary:", person)
print("Person Name:", person["name"])
print("Person Age:", person["age"])
print("Person City:", person["city"])

# 7. Character Variable – single character
grade = 'A'
initial = 'L'
print("Grade:", grade)
print("Initial:", initial)

# 8. None Variable – empty / nothing
result = None
answer = None
print("Result:", result)
print("Answer:", answer)
