# 1. Arithmetic Operators
a = 10
b = 5
print("Arithmetic Operators:")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)       # Remainder
print("Exponent:", a ** b)     # a^b
print("Floor Division:", a // b)
print()

# 2. Comparison Operators
print("Comparison Operators:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)
print()

# 3. Logical Operators
x = True
y = False
print("Logical Operators:")
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)
print()

# 4. Assignment Operators
num = 10
print("Assignment Operators:")
num += 5   # num = num + 5
print("num after += :", num)
num -= 3   # num = num - 3
print("num after -= :", num)
num *= 2   # num = num * 2
print("num after *= :", num)
num /= 4   # num = num / 4
print("num after /= :", num)
print()

# 5. Membership Operators
name = "python"
print("Membership Operators:")
print("'p' in name:", 'p' in name)
print("'z' not in name:", 'z' not in name)
print()

# 6. Identity Operators
a = 5
b = 5
print("Identity Operators:")
print("a is b:", a is b)
print("a is not b:", a is not b)
